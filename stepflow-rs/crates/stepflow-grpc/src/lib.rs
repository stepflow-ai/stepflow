//! gRPC protocol definitions and services for Stepflow.
//!
//! This crate contains:
//! - Generated protobuf types and gRPC service definitions
//! - Service implementations for the orchestrator (RunsService, BlobService, etc.)
//! - Queue-based plugin for dispatching tasks to workers
//! - Task completion registry for correlating results

/// Generated protobuf types for the Stepflow v1 protocol.
pub mod proto {
    pub mod stepflow {
        pub mod v1 {
            tonic::include_proto!("stepflow.v1");
        }
    }

    pub mod google {
        pub mod api {
            tonic::include_proto!("google.api");
        }
    }
}

